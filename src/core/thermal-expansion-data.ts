/** デフォルトの体積膨張係数（データがない場合のフォールバック） */
export const DEFAULT_THERMAL_EXPANSION = 1.0e-3;

/**
 * CAS番号から体積膨張係数を取得する
 * @returns 体積膨張係数 (K⁻¹)、見つからない場合はデフォルト値
 */
export function getThermalExpansion(casNumber: string | null): number {
  if (!casNumber) return DEFAULT_THERMAL_EXPANSION;
  return THERMAL_EXPANSION_COEFFICIENTS[casNumber] ?? DEFAULT_THERMAL_EXPANSION;
}
